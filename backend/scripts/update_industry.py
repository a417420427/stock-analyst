def main():
    conn = sqlite3.connect(DB)
    cur = conn.cursor()

    updated = 0

    # A股用代码映射
    for symbol, industry in A_SHARE_INDUSTRY.items():
        cur.execute(
            "UPDATE stocks SET sector = ? WHERE symbol = ? AND market = 'A' AND (sector IS NULL OR sector = '')",
            (industry, symbol)
        )
        updated += cur.rowcount

    # 港股用代码映射
    for symbol, sector in HK_SHARE_SECTOR.items():
        cur.execute(
            "UPDATE stocks SET sector = ? WHERE symbol = ? AND market = 'HK' AND (sector IS NULL OR sector = '')",
            (sector, symbol)
        )
        updated += cur.rowcount

    # 美股用代码映射
    for symbol, sector in US_SHARE_SECTOR.items():
        cur.execute(
            "UPDATE stocks SET sector = ? WHERE symbol = ? AND market = 'US' AND (sector IS NULL OR sector = '')",
            (sector, symbol)
        )
        updated += cur.rowcount

    conn.commit()

    # 统计剩余无行业的数据
    remaining = cur.execute(
        "SELECT COUNT(*) FROM stocks WHERE sector IS NULL OR sector = ''"
    ).fetchone()[0]

    # 看看哪些还没行业
    nogaps = cur.execute(
        "SELECT symbol, name, market FROM stocks WHERE sector IS NULL OR sector = ''"
    ).fetchall()

    print(f"✅ 更新了 {updated} 条股票行业信息")
    print(f"❓ 仍有 {remaining} 条无行业信息")
    if nogaps:
        for s, n, m in nogaps:
            print(f"   {s} {n} ({m})")

    conn.close()


if __name__ == "__main__":
    main()
